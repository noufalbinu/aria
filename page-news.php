<p>
Aria Holding, a leading global conglomerate based out of Qatar, and the Government of Maharashtra, entered a Memorandum of Understanding (MoU) at the World Economic Forum 2024 Annual Meeting in Davos, Switzerland, under which Aria Holding will invest over INR 2,000 Crores to establish a state-of-the-art float glass manufacturing facility in Maharashtra. The agreement was signed by Mr. Suraj Thampi, Group Chief Executive Officer of Aria Holding, and Dr. Harshdeep Kamble, Principal Secretary of the Industries Ministry, in the presence of the Chief Minister of Maharashtra, Mr. Eknath Shinde, the Minister of Industries, Mr. Uday Samant, and Mr. John Douglas, Commercial Director, Alutec Facades.<br><br>
"The Government of Maharashtra welcomes this investment by Aria Holding, and this MoU marks a significant milestone in our efforts to promote the local economy and attract new investors to the state,” stated His Excellency Mr. Eknath Shinde, Chief Minister of Maharashtra. “We are confident that this investment by Aria Holding will fortify the state's manufacturing sector and create a spectrum of job opportunities, from skilled labour for manufacturing to professionals, and will bring tangible benefits to our state. We are committed to providing all necessary support for successfully establishing the float glass plant.”<br><br>
The MoU aligns with the ‘Make in India’ initiative launched by Prime Minister Narendra Modi in 2014, repositioning India as a critical market to do business in, attracting unprecedented growth in foreign investments. Infrastructure development is core to India’s growth strategy, and float glass plays a pivotal role across sectors, including construction, automotive, and industries. A significant amount of float glass requirements is still met through imports, and demand continues to increase. Aria Holding’s investment will boost the domestic supply of float glass and is intertwined with India's development strategy, supporting infrastructure expansion, automotive growth, energy efficiency, employment generation, and technological advancements. <br><br>
"It is rewarding to see our continued expansion and investment into new markets through this MoU,” remarked His Excellency Sheikh Ali Bin Hamad Al Thani, Chairman, Aria Holding. “India is a compelling destination for investment, and Maharashtra, with its location, workforce, infrastructure, and progressive policies, provides us with a great platform to establish our factories.”<br><br>
The strategic partnership demonstrates the Government of Maharashtra’s dedication to fostering development in the state and providing a conducive environment for businesses to thrive and contribute to its progress.<br><br>

"Aria Holding is excited to be a part of India’s growth story through this strategic investment. Our state-of-the-art factories in Maharashtra will manufacture glass for local, national, and international demand. India is going through a massive boom in infrastructure development nationally, and we plan to capture a significant share of that growth. Through our portfolio companies, we also plan to establish a robust export strategy for our products, " said Suraj Thampi, Group CEO of Aria Holding.</p>
